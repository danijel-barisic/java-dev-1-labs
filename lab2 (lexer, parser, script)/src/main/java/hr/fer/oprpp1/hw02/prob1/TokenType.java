package hr.fer.oprpp1.hw02.prob1;

/**
 * Types of tokens:
 * EOF - end of file
 * WORD - a chunk of text unit separated by whitespace
 * NUMBER - a numeric value of type long
 * SYMBOL - an allowed symbol character
 *
 * @Author Danijel Barišić
 */

public enum TokenType {
    EOF, WORD, NUMBER, SYMBOL
}
